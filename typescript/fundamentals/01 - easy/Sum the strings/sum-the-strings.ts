/*
Create a function that takes 2 integers in form of a string as an input, and outputs the sum (also as a string):
*/

/**
 *
 * @param nA
 * @param nB
 * @returns
 */

const sumTheStrings = (nA: string, nB: string): string => {
  return (Number(nA) + Number(nB)).toString();
};

